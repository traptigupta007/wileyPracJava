public class Loops {
    public static void main(String args[]) {
        System.out.println("for loop ex");
        for (int i = 0; i <= 10; i++)
            System.out.println(i);
        int i = 0;
        System.out.println(" while loop ex");
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
        System.out.println("Do while loop ex");
        i = 0;
        do {
            System.out.println(i);
            i++;
        } while (i <= 10);
    }
}
